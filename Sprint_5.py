# 1. Crear el diccionario inicial
mi_dict = {
    'name': 'Alice',
    'age': 30,
    'city': 'New York'
}

print("Diccionario inicial:", mi_dict)

# 2. Añadir un nuevo par clave-valor
mi_dict['job'] = 'Engineer'

print("\nDiccionario después de añadir 'job':", mi_dict)

# 3. Imprimir el valor asociado con la clave 'age'
print("\nLa edad de Alice es:", mi_dict['age'])

# 4. Actualizar el valor de 'city' y imprimir el diccionario actualizado
mi_dict['city'] = 'San Francisco'

print("\nDiccionario actualizado con nueva ciudad:", mi_dict)